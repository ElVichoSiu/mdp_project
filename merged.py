# juntar el csv
import pandas as pd

bdata = pd.read_csv("books_data.csv")
films = pd.read_csv("films.csv")

# combinar por columnas donde coincidan nombre del libro y fiction_work

merged_df = bdata.merge(films, left_on="Title", right_on="fiction_work")

#a√±o
merged_df["film_year"] = merged_df["film_adaptation"].str.extract(r"\((\d{4})\)")

result = merged_df[["fiction_work", "film_year"]]
result.to_csv("nombres.txt", index=False, header=False, sep=";")

